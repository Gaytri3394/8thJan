package operators;

public class Ifelse1 {
	
	public static void main(String[]args) {
		
		int a = 12;
		int b = 35;
		
		if (a<b) {
			System.out.println("print a");
		}
		else {
			System.out.println("0");
		}
			
		if (a>b) {
			System.out.println("print b");
		}
		else {
			System.out.println("10");
		}
	}

}
